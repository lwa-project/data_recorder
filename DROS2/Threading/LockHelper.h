#ifndef LOCKHELPER_H_
#define LOCKHELPER_H_

#include <boost/thread/mutex.hpp>
#include <boost/thread/recursive_mutex.hpp>
#include <boost/thread/locks.hpp>

//#define LOCK_CHEAT true
#ifdef LOCK_CHEAT
	#define SERIALIZE_ACCESS()
	#define DECLARE_ACCESS_MUTEX() mutable boost::recursive_mutex mtx

	#define SERIALIZE_ACCESS_ST()
	#define DECLARE_ACCESS_MUTEX_ST() static boost::recursive_mutex smtx
	#define INIT_ACCESS_MUTEX_ST(C)   boost::recursive_mutex C::smtx
#else
	#define SERIALIZE_ACCESS(l)   boost::recursive_mutex::scoped_lock BOOST_PP_CAT(BOOST_PP_CAT(lock_,l),_)(BOOST_PP_CAT(BOOST_PP_CAT(mtx_,l),_))
	#define RELEASE_ACCESS_LOCK(l)  BOOST_PP_CAT(BOOST_PP_CAT(lock_,l),_).unlock()
	#define DECLARE_ACCESS_MUTEX(l) mutable boost::recursive_mutex BOOST_PP_CAT(BOOST_PP_CAT(mtx_,l),_)

	#define SERIALIZE_ACCESS_ST(l) boost::recursive_mutex::scoped_lock BOOST_PP_CAT(BOOST_PP_CAT(lock_,l),_)(BOOST_PP_CAT(BOOST_PP_CAT(st_mtx_,l),_))
	#define RELEASE_ACCESS_LOCK_ST(l) BOOST_PP_CAT(BOOST_PP_CAT(lock_,l),_).unlock()
	#define DECLARE_ACCESS_MUTEX_ST(l) static boost::recursive_mutex BOOST_PP_CAT(BOOST_PP_CAT(st_mtx_,l),_)
	#define INIT_ACCESS_MUTEX_ST(C,...)   boost::recursive_mutex C::BOOST_PP_CAT(BOOST_PP_CAT(st_mtx_,__VA_ARGS__),_)
#endif
#endif /* LOCKHELPER_H_ */
